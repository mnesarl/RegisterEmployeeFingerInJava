    private void addHyperlink() {
        JSONObject temp = TKHelper.readJSONFile();
        JSONObject data = (JSONObject) temp.get("program");
        String URL = data.get("url").toString();
        String companyFullName = data.get("company").toString();
        companyName.setCursor(new Cursor(Cursor.HAND_CURSOR));
        companyName.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                try {
                    Desktop.getDesktop().browse(new URI(URL));
                } catch (IOException | URISyntaxException ex) {
                    ex.printStackTrace();
                }
            }

            @Override
            public void mouseExited(MouseEvent e) {
                companyName.setText(companyFullName);
            }

            @Override
            public void mouseEntered(MouseEvent e) {
                companyName.setForeground(new Color(255, 153, 102));
                companyName.setText("<html><a href=''>" + companyFullName + "</a></html>");
            }
        });
    }

